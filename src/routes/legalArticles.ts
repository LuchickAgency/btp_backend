router.get("/:id", async (req, res) => {
  try {
    const { id } = req.params;

    const [article] = await db
      .select()
      .from(legalArticles)
      .where(eq(legalArticles.id, id));

    if (!article) return res.status(404).json({ error: "NOT_FOUND" });

    const tags = await db
      .select()
      .from(legalArticleTags)
      .where(eq(legalArticleTags.articleId, id));

    res.json({ ...article, tags: tags.map((t) => t.tag) });
  } catch (err) {
    console.error("GET ARTICLE ERROR:", err);
    res.status(500).json({ error: "Server error" });
  }
});

/* ---------------------------------------------------------
   PATCH /legal-articles/:id
--------------------------------------------------------- */
router.patch("/:id", requireAuth, async (req, res) => {
  try {
    const userId = (req as any).userId;
    const { id } = req.params;

    if (!(await isSuperAdmin(userId))) {
      return res.status(403).json({ error: "NOT_ALLOWED" });
    }

    const parsed = updateSchema.safeParse(req.body);
    if (!parsed.success) {
      return res.status(400).json({
        error: "INVALID_DATA",
        details: parsed.error.flatten(),
      });
    }

    const { title, body, tags } = parsed.data;

    const updateValues: any = {};
    if (title) updateValues.title = title;
    if (body) {
      updateValues.body = body;
      updateValues.rawContent = body;
    }

    const [updated] = await db
      .update(legalArticles)
      .set(updateValues)
      .where(eq(legalArticles.id, id))
      .returning();

    if (!updated) return res.status(404).json({ error: "NOT_FOUND" });

    if (tags) {
      await db
        .delete(legalArticleTags)
        .where(eq(legalArticleTags.articleId, id));

      if (tags.length > 0) {
        await db.insert(legalArticleTags).values(
          tags.map((t) => ({
            id: crypto.randomUUID(),
            articleId: id,
            tag: t,
          }))
        );
      }
    }

    res.json(updated);
  } catch (err) {
    console.error("PATCH ERROR:", err);
    res.status(500).json({ error: "Update failed" });
  }
});

/* ---------------------------------------------------------
   DELETE /legal-articles/:id
--------------------------------------------------------- */
router.delete("/:id", requireAuth, async (req, res) => {
  try {
    const userId = (req as any).userId;
    const { id } = req.params;

    if (!(await isSuperAdmin(userId))) {
      return res.status(403).json({ error: "NOT_ALLOWED" });
    }

    await db
      .delete(legalArticleTags)
      .where(eq(legalArticleTags.articleId, id));

    await db.delete(legalArticles).where(eq(legalArticles.id, id));

    res.json({ message: "Deleted" });
  } catch (err) {
    console.error("DELETE ERROR:", err);
    res.status(500).json({ error: "Delete failed" });
  }
});

export default router;
